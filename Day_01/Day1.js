// String
var fullname = "Adil Rasul Patel";
console.log(typeof(fullname));

// Boolean
var boolean = false;
console.log(typeof(boolean));

// Object
var obj = {
    firstname: "Adil",
    Age: 24,
    isMarried: false
};
console.log(typeof(obj));

// Array
var arr = [10, 20, 30, 40, 50];
